    public void getAllData() {
        List<UserModel> users = userDao.findAll();
        
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{"Nama", "Username", "Password", "No Telp"}); // Adjust column names as needed

        // Populate the model with data from spareparts
        for (UserModel user : users) {
            model.addRow(new Object[]{user.getName(), user.getUsername(), user.getPassword(), user.getPassword()}); // Add more attributes as needed
        }
        
        // Set the table model to jTable1
        jTable1.setModel(model);
    }
    
