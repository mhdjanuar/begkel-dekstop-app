    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String name = jTextField1.getText();
        String username = jTextField2.getText();
        String password = jTextField3.getText();
        String phoneNumber = jTextField4.getText();
        
        UserModel user = new UserModel();
        user.setName(name);
        user.setUsername(username);
        user.setPassword(password);
        user.setPhoneNumber(phoneNumber);
                
        int result = userDao.upsert(user);
        
        if (result > 0) {
            JOptionPane.showMessageDialog(this, "Data added successfully!");
            getAllData();
            resetField();
        } else {
            JOptionPane.showMessageDialog(this, "Failed to add data.");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jTable1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MousePressed
        // TODO add your handling code here:
        JTable tblUser = (JTable)evt.getSource();
        
        int row = tblUser.getSelectedRow();
        
        UserModel user = new UserModel();
        user.setUsername((String) tblUser.getValueAt(row, 1));
        user.setPassword((String) tblUser.getValueAt(row, 2));
        userList.add(user);
        
        jTextField1.setText((String) tblUser.getValueAt(row, 0));
        jTextField2.setText((String) tblUser.getValueAt(row, 1));
        jTextField3.setText(String.valueOf(tblUser.getValueAt(row, 2)));
        jTextField4.setText(String.valueOf(tblUser.getValueAt(row, 3)));
    }//GEN-LAST:event_jTable1MousePressed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        UserModel userInList = userList.get(0);
        
        UserModel user = userDao.findOneByUsernameAndPassword(userInList.getUsername(), userInList.getPassword());
          
        int confirmDialogResult = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this data?", "Confirm Deletion", JOptionPane.YES_NO_OPTION);
        
        if (confirmDialogResult == JOptionPane.YES_OPTION) {       
              userDao.delete(user);
              userList.remove(0); // Remove the User from the list
              getAllData();
              resetField();
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

